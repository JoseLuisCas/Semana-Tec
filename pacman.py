def change(x, y):
    "Change pacman aim if valid."
    if valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y

def track(pac):
    if pac.x == 5:
        return vector(-5, 0)
    elif pac.x == -5:
        return vector(5, 0)
    elif pac.y == 5:
        return vector(0, -5)
    else:
        return vector(0, -5)
    

